import UIKit

extension UIColor {
    
    /// アプリのメインカラー
    class var appMain: UIColor {
        return UIColor(named: "appMain")!
    }
    
    /// アプリのバックグラウンドカラー
    class var appBackground: UIColor {
        return UIColor(named: "appBackground")!
    }
    
}
